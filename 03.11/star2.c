//피라미드 형태 출력
//     *
//    ***
//   *****
//  *******
// *********

#include <stdio.h>

int main(void)
{
    int i, j, k;
    printf("피라미드의 높이를 입력하세요: ");
    scanf("%d", &i); //사용자로부터 피라미드의 높이를 입력받습니다. 예를 들어, 사용자가 6를 입력하면 i는 6로 설정됩니다.
    for (j = 1; j <= i; ++j) { //j는 현재 피라미드의 층을 나타냅니다. j는 1부터 시작하여 i까지 증가하면서 반복됩니다. 예를 들어, i가 6인 경우, j는 1, 2, 3, 4, 5, 6의 값을 가지며 각 층을 처리합니다.
        for (k = 1; k <= i - j; ++k) { //k는 각 층에서 출력할 공백의 개수를 제어합니다. k는 1부터 시작하여 i - j까지 증가하면서 반복됩니다. 예를 들어, j가 1인 경우, k는 1부터 5까지 반복되어 5개의 공백이 출력됩니다. j가 2인 경우, k는 1부터 4까지 반복되어 4개의 공백이 출력됩니다. 이렇게 해서 각 층마다 필요한 공백이 출력됩니다.
            printf(" ");
        }
        for (k = 1; k <= (2 * j - 1); ++k) { //k는 각 층에서 출력할 별표의 개수를 제어합니다. k는 1부터 시작하여 (2 * j - 1)까지 증가하면서 반복됩니다. 예를 들어, j가 1인 경우, k는 1부터 1까지 반복되어 1개의 별표가 출력됩니다. j가 2인 경우, k는 1부터 3까지 반복되어 3개의 별표가 출력됩니다. 이렇게 해서 각 층마다 필요한 별표가 출력됩니다.
            printf("*");
        }
        printf("\n"); //각 층이 끝난 후 줄 바꿈을 합니다.
    }
    return 0;
}