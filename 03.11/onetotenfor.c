#include<stdio.h>

int main(void)
{
    int i = 0;
    for (; i < 10; ++i) {
        printf("%d ", i + 1);
    }
    printf("%d", i);
    return 0;
}

//이 프로그램은 1부터 10까지의 숫자를 출력하는 간단한 C 프로그램입니다.
//for 루프를 사용하여 0부터 9까지의 숫자를 반복하면서, 각 숫자에 1을 더하여 출력합니다.
//마지막에 printf("%d, i");는 i의 값을 출력하려고 시도하지만,
//i는 for 루프 내에서 선언된 지역 변수이므로 이 부분은 오류가 발생할 수 있습니다. 따라서 이 부분은 제거하거나 수정해야 합니다.